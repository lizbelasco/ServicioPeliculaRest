package com.soa.dto;

/**
 * 
 */
public class Llamada {
    //Para un Map, llamada esta dentro de llamadas
    
    private String noTelefonico;
    private String fecha;
    
    /**
     * @return the noTelefonico
     */
    public String getNoTelefonico() {
        return noTelefonico;
    }
    /**
     * @param noTelefonico the noTelefonico to set
     */
    public void setNoTelefonico(String noTelefonico) {
        this.noTelefonico = noTelefonico;
    }
    /**
     * @return the fecha
     */
    public String getFecha() {
        return fecha;
    }
    /**
     * @param fecha the fecha to set
     */
    public void setFecha(String fecha) {
        this.fecha = fecha;
    }
}
